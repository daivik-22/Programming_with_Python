# Expt 2.1.3 Set Operations
# Define two sets
A = {1, 2, 3, 4, 5}
B = {4, 5, 6, 7, 8}
print("Set A:", A)
print("Set B:", B)
# Union
print("Union (A | B):", A | B)
# Intersection
print("Intersection (A & B):", A & B)
# Difference
print("Difference (A - B):", A - B)
# Symmetric Difference
print("Symmetric Difference (A ^ B):", A ^ B)
# Membership Test
print(f"Is 3 in A? {'Yes' if 3 in A else 'No'}")
